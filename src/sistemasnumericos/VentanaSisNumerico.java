    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String simbolos;
        try {
            simbolos = jTextField1.getText();
            sib = new char[simbolos.length()];
            for (int i = 0; i < simbolos.length(); i++) {
                sib[i] = simbolos.charAt(i);
            }
            sisNumerico = new Sistema(sib);
            jTextField2.setText(String.valueOf(sib.length));

        } catch (ExInvalidArraySize e) {
            JOptionPane.showMessageDialog(this, "ERROR: NO SE PERMITEN MENOS DE 2 SIMBOLOS");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR: CARACTERES NO VALIDOS");
        }
        finally{
            jTextField1.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int no;
        try {
            no = Integer.valueOf(jTextField3.getText());
            if (no < -1) {
                JOptionPane.showMessageDialog(this, "ERRR: NO SE PERMITEN NUMEROS NEGATIVOS");
            } else {
                JOptionPane.showMessageDialog(this, "RESULTADO: " + sisNumerico.DecASimb(no));
            }
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(this, "ERRR: NO EXISTE ALGUN SISTEMA NUMERICO");
        } catch (Exception e){
            JOptionPane.showMessageDialog(this, "ERRR: NO SE INGRESARON NUMEROS");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

